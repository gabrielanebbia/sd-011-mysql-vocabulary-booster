SELECT jobs.JOB_TITLE AS Cargo, ROUND(AVG(emps.SALARY), 2) AS `Média salarial`,
CASE 
WHEN ROUND(AVG(emps.SALARY), 2) BETWEEN 2000 AND 5800 THEN 'Júnior'
WHEN ROUND(AVG(emps.SALARY), 2) BETWEEN 5801 AND 7500 THEN 'Pleno'
WHEN ROUND(AVG(emps.SALARY), 2) BETWEEN 7501 AND 10500 THEN 'Sênior'
WHEN ROUND(AVG(emps.SALARY), 2) > 10500 THEN 'CEO'
END AS Senioridade
FROM hr.employees AS emps
INNER JOIN hr.jobs AS jobs
ON emps.JOB_ID = jobs.JOB_ID
GROUP BY emps.JOB_ID
ORDER BY ROUND(AVG(emps.SALARY), 2) ASC, jobs.JOB_TITLE ASC;
