SELECT
JOB_TITLE AS "Cargo",
ROUND((MIN_SALARY+MAX_SALARY) DIV 2, 2) AS "Média salarial",
CASE
WHEN (MIN_SALARY+MAX_SALARY) DIV 2 < 5800
THEN "Júnior"
WHEN (MIN_SALARY+MAX_SALARY) DIV 2 < 7500
THEN "Pleno"
WHEN (MIN_SALARY+MAX_SALARY) DIV 2 < 10500
THEN "Sênior"
ELSE "CEO"
END AS "Senioridade"
FROM hr.jobs
ORDER BY
(MIN_SALARY+MAX_SALARY) DIV 2 ASC,
JOB_TITLE ASC;
