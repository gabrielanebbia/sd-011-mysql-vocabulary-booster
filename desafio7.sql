SELECT UCASE(CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME)) AS 'Nome completo',
JH.START_DATE  AS 'Data de início',
(
SELECT SALARY
FROM hr.employees
WHERE JH.EMPLOYEE_ID = EMPLOYEE_ID
) AS 'Salário'
FROM hr.employees E
INNER JOIN hr.job_history JH
ON E.EMPLOYEE_ID = JH.EMPLOYEE_ID AND
MONTH(JH.START_DATE) IN (1, 2, 3)
ORDER BY CONCAT(E.FIRST_NAME, ' ', E.LAST_NAME),
JH.START_DATE;
