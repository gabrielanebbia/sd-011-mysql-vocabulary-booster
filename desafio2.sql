SELECT JOB_TITLE AS Cargo,
CASE
WHEN MIN_SALARY AND MAX_SALARY BETWEEN 5000 AND 10000 THEN 'Baixo'
WHEN MIN_SALARY AND MAX_SALARY BETWEEN 10001 AND 20000 THEN 'Médio'
WHEN MIN_SALARY AND MAX_SALARY BETWEEN 20001 AND 30000 THEN 'Alto'
WHEN MAX_SALARY > 30000 THEN 'Altíssimo'
ELSE NULL
END AS Nível
FROM hr.jobs
ORDER BY Cargo ASC;
